function pixel(x, y, griglia) {
	rect(x * griglia, y * griglia, griglia, griglia)

}

function disegnaGriglia(griglia){
	background(255)
	let numX = Math.floor(width / griglia)
	let numY = Math.floor(height / griglia)

	noStroke()


	for( let j=0; j<numY; j=j+1) {
		for( let i=0; i<numX; i=i+1) {
			if(Math.random() < 0.4) {
				fill(255)
			} else {
				fill(0)
			}
			rect( i * griglia, j * griglia, griglia, griglia)
		}
	}
}










